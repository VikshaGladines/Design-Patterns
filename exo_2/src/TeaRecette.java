public class TeaRecette {

    public void Recette() {
        Bouillir();
        Feuille();
        Ajouter();
        Verser();
    }

    public void Bouillir() {
        System.out.println("Faire bouillir l'eau");
    }

    public void Feuille() {
        System.out.println("Mettre vos feuilles de menthe");
    }

    public void Ajouter() {
        System.out.println("Ajouter votre cuillerée de poivre et votre clou de girofle");
    }

    public void Verser() {
        System.out.println("Verser le thé dans une tasse");
    }

}
